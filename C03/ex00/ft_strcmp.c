int	ft_strcmp(char *s1, char *s2)
{
	int	diff;

	while (*s1 == *s2)
	{
		if (*s1 == '\0')
		{
			return (0);
		}
		s1++;
		s2++;
	}
	diff = *s1 - *s2;
	return (diff);
}

